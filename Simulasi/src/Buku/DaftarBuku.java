    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String Kode = txtID.getText();
        String Abstrak = txtAbstrak.getText();
        String Halaman = txtHalaman.getText();
        String ISBN = txtISBN.getText();
        String ISSN = txtISSN.getText();
        String Judul = txtJudul.getText();
        String Pengarang = txtPengarang.getText();
        String Rak = txtRak.getText();
        String Stok = txtStok.getText();
        String Tebal = txtTebal.getText();
        String Kategori = cmbKategori.getSelectedItem().toString();
        
        if(Kode.isEmpty() ||
                Abstrak.isEmpty() ||
                Halaman.isEmpty() ||
                ISBN.isEmpty() ||
                ISSN.isEmpty() ||
                Judul.isEmpty() ||
                Pengarang.isEmpty() ||
                Rak.isEmpty() ||
                Stok.isEmpty() ||
                Tebal.isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap lengkapi data!");
        }else{
            try{
                Connection c = koneksi.GetKoneksi();
                String sql = "INSERT INTO buku VALUES('"+Kode+"',"
                        + "'"+Judul+"', '"+Pengarang+"', '"+ISBN+"', '"+ISSN+"',"
                        + "'"+Halaman+"', '"+Tebal+"', '"+Abstrak+"', '"+Kategori+"',"
                        + "'"+Rak+"', '"+Stok+"')";
                PreparedStatement ps = c.prepareStatement(sql);
                ps.execute();
                ClearAll();
                LoadTable();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int Baris = table.rowAtPoint(evt.getPoint());
        
        String Kode = table.getValueAt(Baris, 0).toString();
        String Abstrak = null;
        String Halaman = null;
        String ISBN = null;
        String ISSN = null;
        String Judul = null;
        String Pengarang = null;
        String Rak = null;
        String Stok = null;
        String Tebal = null;
        String Kategori = null;
        
        try{
            Connection c = koneksi.GetKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM buku WHERE kode_buku = '"+Kode+"'";
            ResultSet rs = s.executeQuery(sql);
            
            while(rs.next()){
                Judul = rs.getString(2);
                Pengarang = rs.getString(3);
                ISBN = rs.getString(4);
                ISSN = rs.getString(5);
                Halaman = rs.getString(6);
                Tebal = rs.getString(7);
                Abstrak = rs.getString(8);
                Kategori = rs.getString(9);
                Rak = rs.getString(10);
                Stok = rs.getString(11);
            }
            
            txtID.setText(Kode);
            txtJudul.setText(Judul);
            txtPengarang.setText(Pengarang);
            txtISBN.setText(ISBN);
            txtISSN.setText(ISSN);
            txtTebal.setText(Tebal);
            txtHalaman.setText(Halaman);
            txtAbstrak.setText(Abstrak);
            cmbKategori.setSelectedItem(Kategori);
            txtRak.setText(Rak);
            txtStok.setText(Stok);
            txtID.setEditable(false);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tableMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        String Kode = txtID.getText();
        
        if(Kode.isEmpty()){
            JOptionPane.showMessageDialog(this, "Tidak ada data yang dihapus");
        }else{
            try{
                Connection c = koneksi.GetKoneksi();
                String sql = "DELETE FROM buku WHERE kode_buku = '"+Kode+"'";
                PreparedStatement ps = c.prepareStatement(sql);
                ps.execute();
                ClearAll();
                LoadTable();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed


