    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int baris = table.rowAtPoint(evt.getPoint());
        String ID = table.getValueAt(baris, 0).toString();
        String Nama = null;
        String Alamat = null;
        String Tanggal = null;
        String Kabupaten = null;
        String KabLahir = null;
        String ProvLahir = null;
        String Pos = null;
        
        try{
            Connection c = koneksi.GetKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT nama, alamat, alamat_kabupaten, tanggal_lahir, kab_lahir, prov_lahir, kode_pos FROM member WHERE nomor_induk = '"+ID+"'";
            ResultSet rs = s.executeQuery(sql);
            
            while(rs.next()){
                Nama = rs.getString(1);
                Alamat = rs.getString(2);
                Kabupaten = rs.getString(3);
                Tanggal = rs.getString(4);
                KabLahir = rs.getString(5);
                ProvLahir = rs.getString(6);
                Pos = rs.getString(7);
            }
            
            txtID.setText(ID);
            txtNama.setText(Nama);
            txtAlamat.setText(Alamat);
            txtKab.setText(Kabupaten);
            txtTanggal.setText(Tanggal);
            txtKabLahir.setText(KabLahir);
            txtProv.setText(ProvLahir);
            txtPos.setText(Pos);
            
            txtID.setEditable(false);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        
    }//GEN-LAST:event_tableMouseClicked

