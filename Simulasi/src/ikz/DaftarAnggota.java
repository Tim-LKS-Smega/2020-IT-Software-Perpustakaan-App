    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String ID = txtID.getText();
        String Nama = txtNama.getText();
        String Alamat = txtAlamat.getText();
        String Tanggal = txtTanggal.getText();
        String Kabupaten = txtKab.getText();
        String KabLahir = txtKabLahir.getText();
        String ProvLahir = txtProv.getText();
        String Pos = txtPos.getText();
        String Status = "Tidak Pinjam";
        
        if(ID.isEmpty() ||
                Nama.isEmpty() ||
                Alamat.isEmpty() ||
                Tanggal.isEmpty() ||
                Kabupaten.isEmpty() ||
                KabLahir.isEmpty() ||
                ProvLahir.isEmpty() ||
                Pos.isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data");
        } else{
            try{
                Connection c = koneksi.GetKoneksi();
                String sql = "INSERT INTO member VALUES ('"+ID+"', '"+Nama+"', '"+Alamat+"', '"+Kabupaten+"', '"+Tanggal+"', '"+KabLahir+"', '"+ProvLahir+"', '"+Pos+"', '"+Status+"')";
                PreparedStatement ps = c.prepareStatement(sql);
                ps.execute(sql);

                ClearAll();
                LoadTable();

                JOptionPane.showMessageDialog(this, "Berhasil Tambah");
            } catch(Exception e){
                JOptionPane.showMessageDialog(this, "Gagal tambah" + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

